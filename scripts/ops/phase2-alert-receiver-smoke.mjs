#!/usr/bin/env node

import { spawnSync } from 'child_process';
import { createServer } from 'http';
import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'fs';
import net from 'net';
import path from 'path';
import { fileURLToPath } from 'url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '../..');
const outputDir = process.env.PHASE2_ALERT_RECEIVER_OUTPUT_DIR || path.join(repoRoot, 'docs/plans');
const startedAt = new Date();
const timestamp = startedAt.toISOString().replace(/[:.]/g, '-');
const projectName = process.env.PHASE2_ALERT_RECEIVER_PROJECT || `sanctuary-phase2-alert-${timestamp.toLowerCase()}`;
const tempDir = path.join(repoRoot, '.tmp', 'phase2-alert-receiver', timestamp);
const composePath = path.join(tempDir, 'compose.yml');
const alertmanagerConfigPath = path.join(tempDir, 'alertmanager.yml');
const keepStack = process.env.PHASE2_ALERT_RECEIVER_KEEP_STACK === 'true';
const timeoutMs = Number(process.env.PHASE2_ALERT_RECEIVER_TIMEOUT_MS || '120000');
const retryMs = Number(process.env.PHASE2_ALERT_RECEIVER_RETRY_MS || '1000');
const alertName = 'Phase2AlertReceiverProof';
const runId = timestamp;

const steps = [];

async function findAvailablePort(startPort) {
  for (let port = startPort; port < startPort + 100; port += 1) {
    // eslint-disable-next-line no-await-in-loop
    const available = await new Promise((resolve) => {
      const server = net.createServer();
      server.once('error', () => resolve(false));
      server.once('listening', () => {
        server.close(() => resolve(true));
      });
      server.listen(port, '127.0.0.1');
    });

    if (available) {
      return port;
    }
  }

  throw new Error(`Could not find an available loopback port starting at ${startPort}`);
}

const alertmanagerPort = process.env.PHASE2_ALERT_RECEIVER_ALERTMANAGER_PORT
  || process.env.ALERTMANAGER_PORT
  || String(await findAvailablePort(19193));
const webhookPort = process.env.PHASE2_ALERT_RECEIVER_WEBHOOK_PORT
  || String(await findAvailablePort(19199));
const alertmanagerUrl = `http://127.0.0.1:${alertmanagerPort}`;
const webhookHostUrl = `http://127.0.0.1:${webhookPort}/alerts`;
const webhookContainerUrl = `http://host.docker.internal:${webhookPort}/alerts`;

function recordStep(name, passed, summary, extra = {}) {
  const step = {
    name,
    passed,
    summary,
    ...extra,
  };
  steps.push(step);
  console.log(`${passed ? 'PASS' : 'FAIL'} ${name}: ${summary}`);
  return step;
}

function runDocker(args, options = {}) {
  const result = spawnSync('docker', args, {
    cwd: tempDir,
    encoding: 'utf8',
    env: process.env,
    maxBuffer: 1024 * 1024 * 20,
    ...options,
  });

  if (result.status !== 0) {
    throw new Error([
      `docker ${args.join(' ')} exited with ${result.status}`,
      result.stdout?.trim(),
      result.stderr?.trim(),
    ].filter(Boolean).join('\n'));
  }

  return result.stdout || '';
}

function runCompose(args) {
  return runDocker(['compose', '-p', projectName, '-f', composePath, ...args]);
}

function writeAlertmanagerConfig() {
  writeFileSync(alertmanagerConfigPath, `# Generated by scripts/ops/phase2-alert-receiver-smoke.mjs
global:
  resolve_timeout: 1m

route:
  group_by: ['alertname', 'severity', 'proof', 'run']
  group_wait: 1s
  group_interval: 1s
  repeat_interval: 1h
  receiver: 'phase2-webhook'

receivers:
  - name: 'phase2-webhook'
    webhook_configs:
      - url: '${webhookContainerUrl}'
        send_resolved: true

inhibit_rules: []
`);
}

function writeComposeFile() {
  writeFileSync(composePath, `services:
  alertmanager:
    image: prom/alertmanager:v0.26.0
    restart: "no"
    extra_hosts:
      - "host.docker.internal:host-gateway"
    ports:
      - "127.0.0.1:${alertmanagerPort}:9093"
    volumes:
      - "./alertmanager.yml:/etc/alertmanager/alertmanager.yml:ro"
    command:
      - "--config.file=/etc/alertmanager/alertmanager.yml"
      - "--storage.path=/alertmanager"
    healthcheck:
      test: ["CMD-SHELL", "wget --no-verbose --tries=1 --spider http://localhost:9093/-/healthy || exit 1"]
      interval: 5s
      timeout: 5s
      retries: 12
      start_period: 5s
`);
}

function validateGeneratedAlertmanagerConfig() {
  const config = readFileSync(alertmanagerConfigPath, 'utf8');
  if (!config.includes('phase2-webhook') || !config.includes(webhookContainerUrl)) {
    throw new Error('Generated Alertmanager config does not include the expected webhook receiver');
  }

  return {
    receiver: 'phase2-webhook',
    webhookUrl: webhookContainerUrl,
  };
}

function startWebhookServer() {
  const received = [];
  const server = createServer((request, response) => {
    const chunks = [];

    request.on('data', (chunk) => {
      chunks.push(chunk);
    });

    request.on('end', () => {
      const body = Buffer.concat(chunks).toString('utf8');

      if (request.method !== 'POST' || request.url?.split('?')[0] !== '/alerts') {
        response.writeHead(404, { 'Content-Type': 'text/plain' });
        response.end('not found');
        return;
      }

      let parsed = null;
      try {
        parsed = body ? JSON.parse(body) : null;
      } catch {
        response.writeHead(400, { 'Content-Type': 'text/plain' });
        response.end('invalid json');
        return;
      }

      received.push({
        receivedAt: new Date().toISOString(),
        method: request.method,
        url: request.url,
        headers: request.headers,
        body: parsed,
      });

      response.writeHead(200, { 'Content-Type': 'application/json' });
      response.end(JSON.stringify({ ok: true }));
    });
  });

  return new Promise((resolve, reject) => {
    server.once('error', reject);
    server.listen(Number(webhookPort), '0.0.0.0', () => {
      server.off('error', reject);
      resolve({ server, received });
    });
  });
}

async function waitForAlertmanagerHealth() {
  const deadline = Date.now() + timeoutMs;
  let latestError;

  while (Date.now() < deadline) {
    try {
      const response = await fetch(`${alertmanagerUrl}/-/healthy`, {
        signal: AbortSignal.timeout(5000),
      });
      const body = await response.text();

      if (response.ok) {
        return 'healthy';
      }

      latestError = new Error(`HTTP ${response.status}: ${body.slice(0, 200)}`);
    } catch (error) {
      latestError = error;
    }

    await new Promise((resolve) => setTimeout(resolve, retryMs));
  }

  throw latestError || new Error('Timed out waiting for Alertmanager health');
}

async function getAlertmanagerConfigStatus() {
  const response = await fetch(`${alertmanagerUrl}/api/v2/status`, {
    headers: { Accept: 'application/json' },
    signal: AbortSignal.timeout(5000),
  });
  const body = await response.text();

  if (!response.ok) {
    throw new Error(`Alertmanager status returned HTTP ${response.status}: ${body.slice(0, 200)}`);
  }

  return {
    reachable: true,
  };
}

async function postTestAlert() {
  const alert = {
    labels: {
      alertname: alertName,
      severity: 'critical',
      proof: 'phase2-alert-receiver',
      run: runId,
    },
    annotations: {
      summary: 'Phase 2 alert receiver delivery proof',
      description: 'Disposable local webhook receiver proof for Alertmanager routing.',
    },
    startsAt: new Date().toISOString(),
    endsAt: new Date(Date.now() + 10 * 60 * 1000).toISOString(),
    generatorURL: 'https://sanctuary.local/phase2-alert-receiver-proof',
  };

  const response = await fetch(`${alertmanagerUrl}/api/v2/alerts`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Accept: 'application/json',
    },
    body: JSON.stringify([alert]),
    signal: AbortSignal.timeout(5000),
  });
  const body = await response.text();

  if (!response.ok) {
    throw new Error(`Alertmanager alert POST returned HTTP ${response.status}: ${body.slice(0, 200)}`);
  }

  return {
    status: response.status,
    alertName: alert.labels.alertname,
  };
}

async function waitForWebhookDelivery(received) {
  const deadline = Date.now() + timeoutMs;

  while (Date.now() < deadline) {
    const match = received.find((delivery) => (
      Array.isArray(delivery.body?.alerts)
      && delivery.body.alerts.some((alert) => (
        alert?.labels?.alertname === alertName
        && alert?.labels?.run === runId
        && alert?.labels?.proof === 'phase2-alert-receiver'
      ))
    ));

    if (match) {
      return match;
    }

    await new Promise((resolve) => setTimeout(resolve, retryMs));
  }

  throw new Error(`Timed out waiting for ${alertName} webhook delivery`);
}

function collectComposePs() {
  return runCompose(['ps', '--format', 'json'])
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => JSON.parse(line))
    .map((container) => ({
      service: container.Service,
      state: container.State,
      health: container.Health || '',
      publishers: container.Publishers || [],
    }));
}

async function waitForComposeHealthy() {
  const deadline = Date.now() + timeoutMs;
  let latestComposePs = [];

  while (Date.now() < deadline) {
    latestComposePs = collectComposePs();
    const unhealthy = latestComposePs.filter((container) => (
      container.state !== 'running' || (container.health && container.health !== 'healthy')
    ));

    if (unhealthy.length === 0) {
      return latestComposePs;
    }

    await new Promise((resolve) => setTimeout(resolve, retryMs));
  }

  const unhealthy = latestComposePs.filter((container) => (
    container.state !== 'running' || (container.health && container.health !== 'healthy')
  ));
  throw new Error(`Unhealthy containers: ${JSON.stringify(unhealthy)}`);
}

function buildMarkdown(report) {
  const delivery = report.webhookDelivery;
  const lines = [
    '# Phase 2 Alert Receiver Delivery Smoke',
    '',
    `Date: ${report.startedAt}`,
    `Status: ${report.passed ? 'Passed' : 'Failed'}`,
    `Compose project: ${report.projectName}`,
    `Alertmanager URL: ${report.alertmanagerUrl}`,
    `Webhook receiver URL (host): ${report.webhookHostUrl}`,
    `Webhook receiver URL (container): ${report.webhookContainerUrl}`,
    '',
    '## Results',
    '',
    ...report.steps.map((step) => `- ${step.passed ? 'PASS' : 'FAIL'} ${step.name}: ${step.summary}`),
    '',
    '## Delivered Alert',
    '',
    delivery?.delivered
      ? `- Alert: ${alertName}`
      : '- Alert: not delivered',
    delivery?.delivered
      ? '- Severity: critical'
      : '- Severity: not delivered',
    delivery?.delivered
      ? '- Status: received'
      : '- Status: not delivered',
    delivery?.delivered
      ? `- Receiver: ${report.generatedReceiverConfig.receiver}`
      : '- Receiver: not delivered',
    delivery?.delivered
      ? `- Run: ${runId}`
      : '- Run: not delivered',
    '',
    '## Containers',
    '',
    ...report.composePs.map((container) => `- ${container.service}: state=${container.state}${container.health ? ` health=${container.health}` : ''}`),
    '',
    '## Notes',
    '',
    '- This proof starts a disposable Alertmanager container with a temporary webhook receiver and posts a real alert to the Alertmanager v2 API.',
    '- The proof verifies receiver routing and delivery to the webhook sink; it does not choose or validate the production external notification channel.',
    '- Production durable receiver delivery remains pending until the notification channel and credentials are chosen.',
  ];

  return `${lines.join('\n')}\n`;
}

mkdirSync(tempDir, { recursive: true });
writeAlertmanagerConfig();
writeComposeFile();

let webhookServer = null;
let receivedWebhookDeliveries = [];
let alertmanagerStatus = null;
let generatedReceiverConfig = null;
let postedAlert = null;
let webhookDelivery = null;
let deliveredCount = 0;
let composePs = [];
let passed = false;
let failureError = null;

try {
  generatedReceiverConfig = validateGeneratedAlertmanagerConfig();
  recordStep('receiver config generated', true, `receiver=${generatedReceiverConfig.receiver} url=${generatedReceiverConfig.webhookUrl}`);

  const webhook = await startWebhookServer();
  webhookServer = webhook.server;
  receivedWebhookDeliveries = webhook.received;
  recordStep('webhook receiver started', true, `listening at ${webhookHostUrl}`);

  runCompose(['up', '-d', 'alertmanager']);
  recordStep('alertmanager started', true, `project=${projectName} alertmanagerPort=${alertmanagerPort}`);

  const health = await waitForAlertmanagerHealth();
  recordStep('alertmanager health', true, health);

  alertmanagerStatus = await getAlertmanagerConfigStatus();
  recordStep('alertmanager status', true, alertmanagerStatus.reachable ? 'status API reachable' : 'status API unavailable');

  postedAlert = await postTestAlert();
  recordStep('test alert submitted', true, `status=${postedAlert.status} alert=${alertName}`);

  webhookDelivery = await waitForWebhookDelivery(receivedWebhookDeliveries);
  deliveredCount = webhookDelivery.body?.alerts?.length || 0;
  recordStep('webhook delivery received', true, `alerts=${deliveredCount} receiver=${generatedReceiverConfig.receiver}`);

  composePs = await waitForComposeHealthy();
  recordStep('alertmanager container health', true, `${composePs.length} service containers running and healthy`);

  passed = steps.every((step) => step.passed);
} catch (error) {
  failureError = error;
  recordStep('phase2 alert receiver smoke', false, error instanceof Error ? error.message : String(error));
  try {
    composePs = collectComposePs();
  } catch {
    composePs = [];
  }
} finally {
  const report = {
    startedAt: startedAt.toISOString(),
    finishedAt: new Date().toISOString(),
    passed,
    projectName,
    alertmanagerUrl,
    webhookHostUrl,
    webhookContainerUrl,
    alertmanagerPort,
    webhookPort,
    steps,
    alertmanagerStatus,
    generatedReceiverConfig,
    postedAlert,
    webhookDelivery: {
      delivered: Boolean(webhookDelivery),
      alertCount: deliveredCount,
    },
    webhookDeliveryCount: receivedWebhookDeliveries.length,
    composePs,
    keptStack: keepStack,
  };

  mkdirSync(outputDir, { recursive: true });
  const mdPath = path.join(outputDir, `phase2-alert-receiver-smoke-${timestamp}.md`);
  const jsonPath = path.join(outputDir, `phase2-alert-receiver-smoke-${timestamp}.json`);
  writeFileSync(mdPath, buildMarkdown(report));
  writeFileSync(jsonPath, `${JSON.stringify(report, null, 2)}\n`);

  console.log(`Wrote ${path.relative(repoRoot, mdPath)}`);
  console.log(`Wrote ${path.relative(repoRoot, jsonPath)}`);

  if (!keepStack) {
    try {
      runCompose(['down', '-v', '--remove-orphans']);
      console.log(`Stopped and removed compose project ${projectName}`);
    } catch (error) {
      console.warn(`Failed to clean up compose project ${projectName}: ${error instanceof Error ? error.message : String(error)}`);
    }
  } else {
    console.log(`Leaving compose project ${projectName} running because PHASE2_ALERT_RECEIVER_KEEP_STACK=true`);
  }

  if (webhookServer) {
    await new Promise((resolve) => {
      webhookServer.close(() => resolve());
    });
  }

  if (!keepStack) {
    rmSync(tempDir, { recursive: true, force: true });
  }
}

if (!passed) {
  if (failureError) {
    console.error(failureError instanceof Error ? failureError.message : String(failureError));
  }
  process.exitCode = 1;
}
